import re

def extract_booking_number(text):
    """Extract booking number from text"""
    print("Searching for booking number...")
    
    # Method 1: Line-by-line search
    lines = text.split('\n')
    print(f"Total lines: {len(lines)}")
    
    for i, line in enumerate(lines):
        if 'Booking #' in line:
            print(f"Found 'Booking #' at line {i+1}: '{line.strip()}'")
            
            # Check next 5 lines for booking number
            for j in range(1, 6):
                if i + j < len(lines):
                    potential_line = lines[i + j].strip()
                    print(f"   Checking line {i+j+1}: '{potential_line}'")
                    
                    # Check if line contains booking number pattern
                    if re.match(r'^[A-Z0-9]{8,12}$', potential_line):
                        print(f"SUCCESS: Found booking number: {potential_line}")
                        return potential_line
    
    print("No booking number found")
    return None

